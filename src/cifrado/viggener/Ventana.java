    private void btn_desActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_desActionPerformed
        // TODO add your handling code here:
        if (txt_key.getText().length() < 1 || txt_msj.getText().length() < 1) {
            JOptionPane.showMessageDialog(null,"Incerta una clave y texto a cifrar");
        }else{
            if (txt_key.getText().length() > txt_msj.getText().length()) {
            
            JOptionPane.showMessageDialog(null,"La clave debe ser menor al tecto a Decifrar");
            
        }else{
            
            jTextField1.setText(vigi.desencriptarTextoCifrado(txt_msj.getText(), txt_key.getText().toUpperCase()));
 
        
        }
        }
        
        
        
    }//GEN-LAST:event_btn_desActionPerformed

